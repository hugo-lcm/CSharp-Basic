int x = 11;
int y = 2;

// adição
Console.WriteLine(x + y);

// subtração
Console.WriteLine(x - y);

// multiplicação
Console.WriteLine(x * y);

// divisao
Console.WriteLine(x / y); // o resto é descartado

// modulo
Console.WriteLine(x % y);

// concatenação de strings
Console.WriteLine("abc" + "def");

// operadores reduzidos
Console.WriteLine(x += 1);
Console.WriteLine(x -= 1);
Console.WriteLine(x *= 1);
Console.WriteLine(x /= 1);